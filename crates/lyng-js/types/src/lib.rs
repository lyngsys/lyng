        let phase6 = js3_promise_builtin();

        assert!(is_js3_internal_builtin(internal));
        assert!(is_js3_internal_builtin(dynamic_import));
        assert!(!is_js3_internal_builtin(public));
        assert!(!is_js3_internal_builtin(phase6));
        assert!(is_js3_core_builtin(public));
        assert!(!is_js3_core_builtin(phase6));
